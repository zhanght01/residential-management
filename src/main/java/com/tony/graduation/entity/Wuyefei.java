package com.tony.graduation.entity;

import java.io.Serializable;

public class Wuyefei implements Serializable {
	private String id;
	private String yezhuid;
	private String yezhuxingming;
	private String zongfeiming;
	private String mingxi;
	private String state;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getYezhuid() {
		return yezhuid;
	}
	public void setYezhuid(String yezhuid) {
		this.yezhuid = yezhuid;
	}
	public String getYezhuxingming() {
		return yezhuxingming;
	}
	public void setYezhuxingming(String yezhuxingming) {
		this.yezhuxingming = yezhuxingming;
	}
	public String getZongfeiming() {
		return zongfeiming;
	}
	public void setZongfeiming(String zongfeiming) {
		this.zongfeiming = zongfeiming;
	}
	public String getMingxi() {
		return mingxi;
	}
	public void setMingxi(String mingxi) {
		this.mingxi = mingxi;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
}
